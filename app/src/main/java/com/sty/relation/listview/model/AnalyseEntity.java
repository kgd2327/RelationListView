public class AnalyseEntity {
    private String name;
    private String retailAmount;
    private String profit;
    private String grossProfit;
    private String receive;
    private String scrap;
    private String profitless;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRetailAmount() {
        return retailAmount;
    }

    public void setRetailAmount(String retailAmount) {
        this.retailAmount = retailAmount;
    }

    public String getProfit() {
        return profit;
    }

    public void setProfit(String profit) {
        this.profit = profit;
    }

    public String getGrossProfit() {
        return grossProfit;
    }

    public void setGrossProfit(String grossProfit) {
        this.grossProfit = grossProfit;
    }

    public String getReceive() {
        return receive;
    }

    public void setReceive(String receive) {
        this.receive = receive;
    }

    public String getScrap() {
        return scrap;
    }

    public void setScrap(String scrap) {
        this.scrap = scrap;
    }

    public String getProfitless() {
        return profitless;
    }

    public void setProfitless(String profitless) {
        this.profitless = profitless;
    }
}
